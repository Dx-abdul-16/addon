package basic.concepts;

public class Program02 {
	public static void main(String[] args) {
		// primitive data type
		// 1. number type
		byte age = 127;
		short b = 32_767;
		int c = 2_147_483_647;
		long d = 9_223_372_036_854_775_007L;
		
		//float type
		float e = 1.123456789F;
		double f = 1.12345678910111245;
		
		// Boolean type
		boolean status = true;
		
		// chart type
		char initial ='R';
		
		// display output
		System.out.println("Byte representive:" + age);
		System.out.println("short sentive:" + b );
		System.out.println("int representive:" + c);
		System.out.println("long repesentive" + d);
		System.out.println("float representive:" + e);
		System.out.println("double representive:" + f);
		System.out.println("boolean representive:" + status);
		System.out.println("char representive:" + initial);
	}

}
